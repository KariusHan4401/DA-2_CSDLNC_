USE QUANLYCONCUNG

GO
CREATE
OR ALTER
PROC USP_TIM_TEN_THUONG_HIEU
	@MATH INT,
	@TEN_TH NVARCHAR(20) OUTPUT
AS
BEGIN
	IF @MATH IS NOT NULL
	BEGIN
		SELECT @TEN_TH = (SELECT TEN_TH
		FROM THUONG_HIEU
		WHERE MATH = @MATH)
	END
END

GO
CREATE
OR ALTER
PROC USP_TIM_LOAI_HANG
	@MALH INT,
	@TEN_LH NVARCHAR(20) OUTPUT
AS
BEGIN
	IF @MALH IS NOT NULL
	BEGIN
		SELECT @TEN_LH = (SELECT TEN_LH
		FROM LOAI_HANG
		WHERE MALH = @MALH)
	END
END
GO